<template>
    <div>
        <h1>Hola, soy una p√°gina Inertia.js con Vue.js!</h1>
        <button @click="getData">Obtener Datos</button>
        <p>{{ message }}</p>
    </div>
</template>

<script>
export default {
    data() {
        return {
            message: ''
        }
    },
    methods: {
        getData() {
            axios.get('/api/data')
                .then(response => {
                    this.message = response.data.message;
                })
                .catch(error => {
                    console.error(error);
                });
        }
    }
}
</script>
